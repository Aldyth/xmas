				<h3><span><span><strong>&nbsp;</strong>Kado Natal Istimewa</span></span></h3>
				<p align="justify">
					</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Ada apa?" Tanyaku ketika memasuki dapur dan kulihat ayahku duduk di kursi dapur. Kepalanya yang beruban bertumpu pada tangannya. Pada hari Sabtu, aku singgah untuk mengunjungi ayahku. Kebiasaan ini kulakukan setelah kematian ibuku setahun yang lalu. Ketika mendengar suaraku, ayah menengadah. "Aku perlu ke dokter. Aku merasa tak sehat."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Tetapi, Dokter Halloway sedang keluar kota," kataku. "Dia mengatakannya kepadamu di gereja Minggu lalu. Ia akan melewatkan Natal dengan memancing."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ketika sekilas kulihat mukanya pucat pasi dan mulutnya berkerut kesakitan, aku tahu bahwa dia memang sakit. Aku meraih telepon dan menghubungi kamar bedah. Aku mendengarkan jawaban yang sudah direkam dan kututup telepon itu. "Ayah, hari ini hari Natal dan kamar bedah tidak buka. Aku akan mengantarmu ke ruang gawat darurat di rumah sakit St. George. Mungkin, ayah makan sesuatu yang tidak cocok. Ayah akan baik-baik saja setelah beberapa jam." Aku membantunya berdiri dari kursi dan memapahnya menuju pintu depan. Kemudian, kami menuju ke tempat parkir mobil.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Selama perjalanan ke rumah sakit, aku menjaga agar pembicaraan kami seputar hal-hal yang ringan saja. Misalnya, tentang kesibukan dua putraku di sekolah. Ayah mengangguk-angguk, tetapi aku dapat melihat dari kernyit keningnya bahwa rasa sakit belum hilang dari tubuhnya. Aku menuju ruang gawat darurat rumah sakit itu 25 menit kemudian. Kuparkir mobilku dan kami menuju ke rumah sakit bersama-sama. Aku menghabiskan waktu 2 jam untuk minum kopi gratis dan membaca tuntas semua majalah yang tersedia di atas meja kayu pada area ruang tunggu. Saat itu, tidak ada sesuatu pun yang bisa kukerjakan. Aku menutup mataku dan mulai berpikir kembali kepada beberapa kunjungan untuk ayah akhir-akhir ini.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Meskipun dia batuk, sepertinya ia baik-baik saja saat itu. Aku menyarankan agar dia ke dokter untuk memeriksakan batuknya. Ia mengingatkanku bahwa ini hanya alergi dan dia memunyai persediaan obat alergi yang cukup untuk seumur hidupnya.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Nona Riley," suara yang berat berseru di dekat telingaku.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Aku membuka mata karena terkejut. Aku menengadah dan menatap wajah dr. Luther. Ia adalah salah satu dokter keluarga yang berpraktik di tempat dr. Halloway. "Kami sedang mencoba untuk menghubungi dr. Halloway. Aku telah memeriksa ayah Anda dan Anda perlu menandatangani beberapa surat."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Surat?" aku menggumam. Kulihat dua helai kertas putih di tangannya.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Anda harus mengizinkan kami untuk mengoperasinya."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Operasi? Ayah hanya makan sesuatu yang tak cocok buatnya," kataku. "Dia tak perlu dioperasi."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Saya khawatir bahwa sakit perutnya sedikit lebih serius," kata dr. Luther. Lebih serius? Perutku mual karena kata serius.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Sinar X menunjukkan adanya sebuah noda hitam. Saya ingin melakukan pemeriksaan lebih lanjut," kata dr. Luther.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ketika aku duduk di ruang tunggu, aku mendengar gemerisiknya seragam para perawat yang melangkah di gang. Mereka menuju ke salah satu kamar di rumah sakit itu. Beberapa menit kemudian, mereka muncul kembali dan meneruskan langkah mereka. Pengumuman terdengar keras dari sistem pengeras suara di langit-langit di atasku beberapa kali sehingga memecahkan kesunyian.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dua jam kemudian, aku menoleh karena kudengar langkah seseorang yang menuju ke kamar tunggu. Dengan kedua tangan di saku dan stetoskop dikalungkan di leher, dr. Luther masuk dan berjalan ke arahku. Tak ada senyum di wajahnya. Kerongkonganku terasa tersumbat. Kupikir, ayah mungkin tidak selamat. "Ia baik-baik saja, bukan?" aku berkata tanpa berpikir.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Nona Riley, ayahmu ada di ruang perawatan intensif. Ia akan tidur untuk beberapa jam yang akan datang."
Perawatan intensif? "Tetapi dokter, bukankah ia bisa sembuh?"

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ia membasahi bibirnya sebelum berbicara. "Beberapa jam berikutnya akan sangat menentukan. Kami terpaksa membuang lapisan perutnya untuk menyingkirkan kanker itu... lalu jantungnya membuat kami khawatir. Sudah berapa lama ia sakit jantung?"

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kanker? Sakit jantung? "Tetapi ayah tak pernah sakit," kataku dengan kebingungan yang tak mungkin kusembunyikan. "Ia mengunjungi dokter hanya untuk mengobati alerginya. Seingatku, ia tak pernah sakit parah." Aku tak dapat mengendalikan pikiranku yang berkecamuk. Bagaimana mungkin ini dapat terjadi? Dan, ini terjadi pada ayahku? "Anda bisa menyingkirkan kankernya? Seluruhnya?" aku menanyakannya.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dr. Luther mengangguk. "Kami akan berusaha. Aku akan masuk untuk memeriksanya nanti." Aku mengucapkan terima kasih dan menuju ke kamar ayahku. Ketika aku masuk dan menuju ke tempat tidurnya, sebuah lonceng di kejauhan berdentang tiga kali. Aku memandang muka putih berkeriput dari pria yang berbaring di tempat tidur dengan pipa-pipa di hidungnya. Ayahku berusia 60 tahun. Tetapi, ia kelihatan lebih tua dari usianya. Sepertinya, ia sudah berusia 70 tahun. Bagaimana aku bisa tidak tahu bahwa ia menjadi tua secepat itu? Aku heran. Apa yang terjadi dengan si pejuang yang pernah kukenal? Kulihat, tak ada perjuangan pada pria yang tidur di hadapanku. Kusentuh tangan rapuhnya yang tergeletak di atas penutup tempat tidur berwarna putih. Aku hampir tak dapat merasakan denyut nadinya. Hanya beberapa suara biip dari alat monitor jantung di dekat sisi tempat tidurnya. Suara alat monitor itu memecahkan kesunyian yang mengerikan.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Tiba-tiba, setetes air mata yang hangat mengalir di pipiku. Bagaimana suasananya bila aku bisa mengatakan kepadanya beberapa tahun lalu bahwa aku mencintainya? Aku menyukai masa kecilku ketika aku duduk di pangkuannya dan ia berkisah tentang cerita anak-anak. Ketika aku beranjak remaja, aku sangat menghargai bantuannya untuk memecahkan soal-soal matematika yang sulit bagiku. Sekarang, kusadari bahwa aku mungkin tidak memunyai kesempatan lagi berbincang dengannya.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kuangkat tangannya ke pipiku dan kurasakan kesejukan di kulitku yang panas. "Tuhan yang tercinta," doaku, "berilah aku kesempatan untuk mengatakan segala sesuatu yang ingin kukatakan. Aku tak pernah memunyai waktu untuk mengatakannya." Kulepaskan tangan Ayah dan aku menjatuhkan diri di atas kursi dekat tempat tidur. Aku memandang wajahnya selama beberapa jam. Aku berharap agar ayahku bisa membuka matanya dan aku memunyai kesempatan lagi untuk menjadikannya sebagai ayah seperti pada masa lalu.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Akhirnya, Suster Sutton yang bertugas pada siang itu masuk ke ruangan tanpa bersuara. Ia mengenakan sepatu putihnya yang beralas halus. Sepatunya bisa mengeluarkan bunyi khas bila ia melangkahkan kaki di lantai yang mengkilat. Ia membaca kartu, memeriksa denyut nadi ayahku, menulis keterangan di kartu, dan meninggalkan ruangan. Menjelang pukul 18.00, ayahku tidak memperlihatkan tanda-tanda sadar sedikit pun. Aku mulai panik. Bagaimana bila ia tak pernah bisa sadar? Bagaimana kalau aku tak akan pernah dapat berbicara lagi dengannya? Perutku mual. Kuambil sapu tanganku dan aku membuang ingus di hidungku.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Apakah Anda baik-baik saja?" suara halus seorang wanita berkata di sampingku. Aku menoleh dan melihat segurat wajah seperti boneka porselen Cina dengan guntingan rambut berwarna seperti bunga matahari. Ia memakai kerudung putih. Ia berdandan dalam seragam putih seperti Suster Sutton tetapi tanpa label nama.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Maaf?" kataku.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Aku mendengar Anda menangis," katanya. "Dapatkah saya membantu?"

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Ayahku tidak sadar lebih dari 3 jam. Aku rasa ia tidak mau berjuang," kataku. "Ini bukanlah dia. Ia biasanya seorang pejuang."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Perawat itu menyentuh lenganku. "Ia sedang beristirahat setelah menghadapi perjuangan yang hebat," katanya. Lalu ia berjalan ke tempat tidur. Jari-jarinya yang lentur menggenggam sekeliling pergelangan tangan ayahku. Ia melihat ke arlojinya. Beberapa menit kemudian, sebuah senyuman melintas di bibirnya yang merah jambu. "Itu lebih baik." Ia melepaskan pergelangan tangan ayah.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Perjuangan yang hebat?" tanyaku. Aku agak bingung dengan komentarnya.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Betul," katanya. "Ia sudah melalui banyak hal. Bila ia bangun, ia akan merasa sangat tidak enak. Anda harus membantunya untuk mengatasi hal ini. Anda harus memperlihatkan kepadanya bahwa masih banyak hal yang bisa dilakukannya agar tetap hidup."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Aku mengangguk. Kupandang ayahku dan memang masih banyak hal yang perlu dilakukannya untuk tetap hidup. Aku menoleh untuk mengucapkan terima kasih kepadanya. Tetapi, ruangan itu kosong. Aku bergegas ke pintu dan mengintip ke luar ruangan. Tak seorang pun yang terlihat.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Aku kembali ke tempat tidur dan kursiku. Kemudian, aku membuat surat pendek untuk berterima kasih kepadanya bila ia kembali.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Tiga puluh menit kemudian, dr. Luther muncul dan memeriksa kartu di kaki tempat tidur. Lalu, ia memandang ayahku dan ke alat monitor. "Bagus. Pipinya mulai berwarna lagi. Denyut jantungnya mulai bertambah kuat."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Jadi, ia akan sembuh?"

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dr. Luther memandangku. "Kami masih harus menunggu dan melihat. Aku akan minta perawat untuk memeriksa denyut nadinya."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Tetapi, perawat baru saja masuk," kataku.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dr. Luther menggaruk kepalanya. "Aneh! Hanya ada satu catatan yang ditulis di kartu."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kemudian, Suster Sutton masuk. "Aku akan memeriksa denyut nadi Mr. Riley."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dr. Luther menoleh. "Bukankah Anda baru saja memeriksanya?"

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Tetapi Dokter," aku menyela, "Maksudku perawat yang satunya."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dr. Luther tersenyum padaku. "Miss Riley, aku rasa Anda perlu beristirahat. Suster Sutton adalah satu-satunya perawat yang bertugas siang dan ditugaskan untuk ini. Perawat yang bertugas malam baru akan datang 3 jam lagi."

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Waktu itu, suara dengkuran menarik perhatian kami. Mata ayahku berkedip dan terbuka. Sorot matanya berpusat ke arahku. Ia tersenyum. "Di mana perawat itu tadi?" ia berbisik. "Ia memunyai sentuhan malaikat?" Lalu, matanya terpejam lagi. Ketika ayahku tidur kembali, satu-satunya suara yang terdengar di dalam ruangan ialah suara biip, biip, dan biip dari monitor jantung.
