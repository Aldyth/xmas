</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Kebiasaan-kebiasaan baru saat Natal muncul pada abad pertengahan. Kontribusi yang paling menonjol adalah lagu-lagu Natal, yang menjelang abad XIV dihubungkan dengan ibadah kelahiran Kristus.

</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Di Italia, sebuah tradisi dikembangkan untuk menetapkan kembali kelahiran Kristus dan penafsiran peristiwa Natal Pertama. Konon, tradisi ini diperkenalkan oleh Santo Fransiskus sebagai bagian dari usaha-usahanya untuk menghadirkan pemahaman rohani kepada kaum awam.

