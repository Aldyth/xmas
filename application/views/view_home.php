</br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Di Babilonia dan Mesir Kuno ada festival tengah musim dingin, dan di Jerman ada festival panen yang dilaksanakan pada masa itu juga. Kelahiran Attis, dewa matahari kuno di Frigia dirayakan pada tanggal 25 Desember, seperti halnya kelahiran Mithras, dewa matahari bangsa Persia. Bangsa Romawi merayakan Saturnalia, sebuah festival yang dipersembahkan untuk Saturnus -- dewa kedamaian dan kemakmuran, yang berlangsung dari tanggal 17 hingga 24 Desember. Tempat-tempat umum dihiasi dengan bunga-bunga; hadiah-hadiah dan lilin-lilin saling ditukarkan. Para penduduk, baik budak-budak maupun majikan-majikan, merayakan peristiwa ini dengan sangat antusias.

